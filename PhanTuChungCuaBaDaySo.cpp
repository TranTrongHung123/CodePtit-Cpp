#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		ll n, m, k;
		cin >> n >> m >> k;
		ll a[n], b[m], c[k];
		vector<ll> vt;
		for(auto &x : a) cin >> x;
		for(auto &x : b) cin >> x;
		for(auto &x : c) cin >> x;
		ll x = 0, y = 0, z = 0;
		while(x < n && y < m && z < k){
			if(a[x] == b[y] && a[x] == c[z]){
				vt.push_back(a[x]);
				++x; ++y; ++z;
			}
			else if(a[x] < b[y]) ++x;
			else if(b[y] < c[z]) ++y;
			else if(c[z] < a[x]) ++z;			
		}
		if(vt.size() == 0){
			cout << "-1" << endl;
		}
		else{
			for(auto x : vt){
				cout << x << " ";
			}
			cout << endl;
		}
	}
}

