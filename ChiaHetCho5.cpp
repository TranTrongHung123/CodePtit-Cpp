#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	int t;
	cin >> t;
	while(t--){
		string s;
		cin >> s;
		ll k = 0, x = 1;
		for(int i = s.size()-1; i >= 0; i--){
			k += (s[i] - '0') * x;
			k %= 5;
			x *= 2;
			x %= 5;
		}
		if(k == 0) cout << "Yes" << endl;
		else cout << "No" << endl;
	}
}

