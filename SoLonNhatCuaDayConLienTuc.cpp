#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n, k;
		cin >> n >> k;
		int a[n];
		for(auto &x : a) cin >> x;
		multiset<int> st;
		for(int i = 0; i < k; i++){
			st.insert(a[i]);
		}
		cout << *st.rbegin() << " ";
		for(int i = k; i < n; i++){
			st.erase(st.find(a[i-k]));
			st.insert(a[i]);
			cout << *st.rbegin() << " ";
		}
		cout << endl;
	}
}

