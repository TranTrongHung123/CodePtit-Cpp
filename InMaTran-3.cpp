#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int TC;
	cin >> TC;
	while(TC--){
		int n, m;
		cin >> n >> m;
		int a[n+1][m+1];
		vector<int> X[n+m];
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				cin >> a[i][j];
				X[i+j-1].push_back(a[i][j]); // Cho cac phan tu vao duong cheo tuong ung
			}
		}
		for(int stt = 1; stt <= n+m-1; stt++){	
			if(stt % 2 == 1){
				for(int idx = X[stt].size()-1; idx >= 0; idx--){
					cout << X[stt][idx] << " ";
				}
			}
			else{
				for(int idx = 0; idx < X[stt].size(); idx++){
					cout << X[stt][idx] << " ";
				}				
			}
		}
		cout << endl;
	}
}

