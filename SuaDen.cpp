#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int n, k, b;
	cin >> n >> k >> b;
	set<int> st;
	while(b--){
		int x;
		cin >> x;
		st.insert(x);
	}
	int a[n + 1];
	for(int i = 1; i <= n; i++){
		a[i] = i;
	}
	for(int i = 1; i <= n; i++){ // Den bi hong danh dau la -1
		if(st.find(a[i]) != st.end()){
			a[i] = -1;
		}
	}	
	int cnt = 0;
	for(int i = 1; i <= k; i++){ // Tim cua so it den bi hong nhat
		if(a[i] == -1) ++cnt;
	}	
	int pos = cnt;
	for(int i = k+1; i <= n; i++){
		if(a[i] == -1) ++cnt;
		if(a[i - k] == -1) --cnt;
		pos = min(pos, cnt);
	}
	cout << pos << endl;
}

