#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

bool check(string s){
	if(s.size() > 9) return false;
	for(auto x : s){
		if(!isdigit(x)){
			return false;
		}
	}
	return true;
}

int main(){
	ifstream in;
	in.open("DATA.in");
	string s;
	ll sum = 0;
	while(in >> s){
		if(check(s)){
			sum += 1ll * stoi(s);
		}
	}
	cout << sum;
}

