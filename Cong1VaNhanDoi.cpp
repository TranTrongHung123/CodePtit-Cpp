#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		int cnt_1 = 0, cnt_2 = 0;
		for(int i = 0; i < n; i++){
			int cnt_max = 0; // Dem so thao tac giam 2 lan nhieu nhat de dua ve day ban dau
			while(a[i]){
				if(a[i] % 2 == 1){
					--a[i];
					++cnt_1;
				}
				else{
					a[i] /= 2;
					++cnt_max;
				}
			}
			cnt_2 = max(cnt_max, cnt_2);
		}
		cout << cnt_1 + cnt_2 << endl;
	}
}

