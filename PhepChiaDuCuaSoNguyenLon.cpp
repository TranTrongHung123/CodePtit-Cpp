#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		string s;
		ll n;
		cin >> s >> n;
		ll result = 0;
		for(auto x : s){
			result = (10 * result + (x - '0')) % n;
		}
		cout << result << endl;
	}
}

