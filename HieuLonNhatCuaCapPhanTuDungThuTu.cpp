#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		int res = 0, ok = false;
		for(int i = 0; i < n; i++){
			for(int j = 0; j < i; j++){
				if(a[i] > a[j]){
					res = max(res, a[i] - a[j]);
					ok = true;
				}
			}
		}	
		if(ok) cout << res << endl;
		else cout << -1 << endl;
	}
}

