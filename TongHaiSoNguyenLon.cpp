#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

string Sum(string s1, string s2){
	int len = max(s1.size(), s2.size());
	while(s1.size() != len) s1 = "0" + s1;
	while(s2.size() != len) s2 = "0" + s2;
	if(s2 > s1) swap(s1,s2);
	int nho = 0;
	string s = "";
	for(int i = s1.size()-1; i >= 0; i--){
		int x = ((s1[i] - '0') + (s2[i] - '0') + nho);
		s = to_string(x % 10) + s;
		nho = x / 10;
	}
	if(nho){
		s = to_string(nho) + s;
	}
	return s;
}

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		string s1, s2;
		cin >> s1 >> s2;
		cout << Sum(s1,s2) << endl;
	}
}

