#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		ll n, X, sum_arr = 0;
		cin >> n >> X;
		ll a[n];
		for(ll i = 0; i < n; i++){
			cin >> a[i];
			sum_arr += a[i];
		}	
		if(sum_arr <=  X){
			cout << -1 << endl;
			continue;
		}
		ll left = 0;
		ll sum = 0, res = 1e9;
		for(ll right = 0; right < n; right++){
			sum += a[right];
			while(sum > X){
				res = min(res, right + 1 - left);
				sum -= a[left];
				++left;
			}
		}
		cout << res << endl;
	}
}

