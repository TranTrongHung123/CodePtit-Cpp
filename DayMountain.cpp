#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

bool check(int a[], int l, int r){
	vector<int> vt;
	for(int i = l; i <= r; i++){
		vt.push_back(a[i]);
	}
	int x;
	if(vt.size() % 2 == 0){
		x = (vt.size() / 2) - 1;
	}
	else{
		x = (vt.size() / 2);
	}
	for(int i = 1; i <= x; i++){
		if(vt[i] < vt[i-1]){
			return false;
		}
	}
	return true;
}

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		int l, r;
		cin >> l >> r;
		if(check(a,l,r)) cout << "Yes" << endl;
		else cout << "No" << endl;
	}
}

