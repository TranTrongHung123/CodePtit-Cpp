#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		string s;
		cin >> s;
		map<char,int> mp;
		for(auto x : s){
			++mp[x];
		}
		int max_fre = 0;
		for(auto x : mp){
			max_fre = max(max_fre, x.second); // Tim ki tu xuat hien nhieu nhat
		}
		int x = s.size() - max_fre; // So luong ki tu con lai trong s
		int y = max_fre - 1; // So vach ngan can lap day
		if(x < y) cout << 0 << endl;
		else cout << 1 << endl;
	}
}

