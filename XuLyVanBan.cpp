using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

bool check(string s){
	if(s[s.size() - 1] == '.' || s[s.size() - 1] == '?' || s[s.size() - 1] == '!') return true;
	return false;
}

string chuanhoa(string s){
	for(auto &x : s){
		x = tolower(x);
	}
	return s;
}

int main(){
	vector<string> vt;
	string s;
	int pos = 0;
	while(cin >> s){
		s = chuanhoa(s);
		++pos;
		if(pos == 1) s[0] = toupper(s[0]);
		if(check(s)){
			s.erase(s.size() - 1);
			cout << s << endl;
			pos = 0;
		}
		else{
			cout << s << " ";
		}
	}
}

