#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

ll luythua(ll a, ll b){ // (a^b) % mod
	ll res = 1;
	while(b){
		if(b % 2 == 1){
			res *= a;
			res %= mod;
		}
		b /= 2;
		a = ((a%mod) * (a%mod)) % mod;
	}
	return res;
}

int main(){
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		ll a[n];
		for(auto &x : a) cin >> x;
		ll h = 1, g = a[0];
		for(auto x : a){
			h *= x;
			h %= mod;
			
		}
		h %= mod;
		for(int i = 1; i < n; i++){
			g = __gcd(g, a[i]);
		}
		cout << luythua(h,g) << endl;
	}
}

