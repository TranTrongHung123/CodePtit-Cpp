#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int a[205][205];
int prefix[205][205];

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		memset(prefix, 0, sizeof(prefix));
		int n, m;
		cin >> n >> m;
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				cin >> a[i][j];
			}
		}
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				prefix[i][j] = a[i][j] + prefix[i][j-1] + prefix[i-1][j] - prefix[i-1][j-1];
			}
		}
		int res = -1e9;
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				for(int k = i; k <= n; k++){
					for(int h = j; h <= m; h++){
						res = max(res, prefix[k][h] - prefix[k][j-1] - prefix[i-1][h] + prefix[i-1][j-1]);
					}
				}
			}
		}
		cout << res << endl;
	}
}

