#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	int t;
	cin >> t;
	cin.ignore();
	map<string,int> mp;
	while(t--){
		string s;
		getline(cin, s);
		for(auto &x : s){
			x = tolower(x);
		}
		stringstream ss(s);
		string token;
		vector<string> vt;
		while(ss >> token){
			vt.push_back(token);
		}
		string tmp = vt[vt.size() - 1];
		for(int i = 0; i < vt.size() - 1; i++){
			tmp += string(1, vt[i][0]);
		}
		++mp[tmp];
		if(mp[tmp] == 1){
			cout << tmp << "@ptit.edu.vn";
		}
		else{
			cout << tmp << mp[tmp] << "@ptit.edu.vn";
		}
		cout << endl;
	}
}

