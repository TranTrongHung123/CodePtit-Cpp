int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		string s;
		cin >> s;
		vector<char> vt;
		for(int i = 0; i < s.size(); i++){
			vt.push_back(s[i]);
			while(vt.size() >= 3 && vt[vt.size()-1] == '0' && vt[vt.size()-2] == '0' && vt[vt.size()-3] == '1'){
				vt.pop_back();
				vt.pop_back();
				vt.pop_back();
			}
		}
		cout << s.size() - vt.size() << endl;
	}
}

