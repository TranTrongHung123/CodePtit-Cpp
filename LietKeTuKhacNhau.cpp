#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

string chuanhoa(string s){
	for(auto &x : s) x = tolower(x);
	return s;
}

int main(){
	ifstream in;
	in.open("VANBAN.in");
	string s;
	map<string,int> mp;
	while(in >> s){
		mp[chuanhoa(s)] = 1;
	}
	for(auto x : mp){
		cout << x.first << endl;
	}
}

