#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n, k;
		cin >> n >> k;
		int a[n];
		for(auto &x : a) cin >> x;
		int sum = 0;
		for(int i = 0; i < k; i++){
			sum += a[i];
		}
		int res = sum, pos = 0;
		for(int i = k; i < n; i++){
			sum = sum + a[i] - a[i-k];
			if(sum > res){
				pos = i;
				res = sum;
			}
		}
		for(int i = pos - k + 1; i <= pos; i++){
			cout << a[i] << " ";
		}
		cout << endl;
	}
}

