#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n], b[n];
		for(auto &x : a) cin >> x;
		for(auto &x : b) cin >> x;
		int res = 0;
		for(int i = 0; i < n; i++){
			int sum1 = 0, sum2 = 0;
			for(int j = i; j < n; j++){
				sum1 += a[j];
				sum2 += b[j];
				if(sum1 == sum2){
					res = max(res, j + 1 - i);
				}
			}
		}
		cout << res << endl;
	}
}

