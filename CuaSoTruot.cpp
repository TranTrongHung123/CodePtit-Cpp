#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int n;
	cin >> n;
	ll A[n][n];
	for(int i = 0; i < n; i++){
		for(int j = 0; j < n; j++){
			cin >> A[i][j];
		}
	}
	int m;
	cin >> m;
	ll B[m][m];
	for(int i = 0; i < m; i++){
		for(int j = 0; j < m; j++){
			cin >> B[i][j];
		}
	}
	ll C[n][n];
	for(int i = 0; i < n; i += m){
		for(int j = 0; j < n; j += m){
			for(int h = i; h < i + m; h++){
				for(int k = j; k < j + m; k++){
					C[h][k] = A[h][k] * B[h-i][k-j];
				}
			}
		}
	}
	for(int i = 0; i < n; i++){
		for(int j = 0; j < n; j++){
			cout << C[i][j] << " ";
		}
		cout << endl;
	}
}

