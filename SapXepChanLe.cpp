#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		vector<int> vt;
		for(int i = 0; i < n; i++){
			cin >> a[i];
			vt.push_back(a[i]);
		}
		sort(vt.begin(), vt.end());
		int idx = 0;
		for(int i = 0; i < n; i += 2){
			a[i] = vt[idx++];
		}	
		for(int i = 1; i < n; i += 2){
			a[i] = vt[idx++];
		}
		for(auto x : a) cout << x << " ";
		cout << endl;		
	}
}

