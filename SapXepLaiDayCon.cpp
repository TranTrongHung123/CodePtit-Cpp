#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const ll mod = 1e9 + 7;

int main(){
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int t;
    cin >> t;
    while(t--){
        int n;
        cin >> n;
        int a[n+1], b[n+1];
        for(int i = 1; i <= n; i++){
            cin >> a[i];
            b[i] = a[i];
        }
        sort(b + 1, b + n + 1);
        int l, r;
        for(int i = 1; i <= n; i++){
            if(a[i] != b[i]){
                l = i;
                break;
            }
        }
        for(int i = n; i >= 1; i--){
            if(a[i] != b[i]){
                r = i;
                break;
            }
        }
        cout << l << " " << r << endl;
    }
}

