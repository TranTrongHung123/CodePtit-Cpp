#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		int res = 1e9;
		int x, y;
		for(int i = 0; i < n; i++){
			for(int j = i+1; j < n; j++){
				if(abs(a[i] + a[j]) < res){
					res = abs(a[i] + a[j]);
					x = i; y = j;
				}
			}
		}
		cout << a[x] + a[y] << endl;
	}
}

