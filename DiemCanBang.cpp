#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int n, a[1000000], prefix[1000000];

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		cin >> n;
		for(int i = 1; i <= n; i++){
			cin >> a[i];
		}
		for(int i = 1; i <= n; i++){
			prefix[i] = prefix[i-1] + a[i];
		}		
		bool ok = false;
		for(int i = 2; i <= n-1; i++){
			if(prefix[i-1] == (prefix[n] - prefix[i])){
				ok = true;
				cout << i << " ";
			}
		}
		if(ok == false){
			cout << "-1";
		}
		cout << endl;
	}
}

