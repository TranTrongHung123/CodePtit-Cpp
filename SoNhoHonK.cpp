#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n, k;
		cin >> n >> k;
		int a[n];
		int l = 0; // Chieu dai cua cua so chua nhieu phan tu nho hon bang k nhat
		for(int i = 0; i < n; i++){
			cin >> a[i];
			if(a[i] <= k){
				++l;
			}
		}
		int cnt = 0;
		for(int i = 0; i < l; i++){ // So luong so nho hon k o cua so dau tien
			if(a[i] <= k){
				++cnt;
			}
		}
		int res = cnt;
		for(int i = l; i < n; i++){ // tim cua so tiep theo co nhieu so <= k nhat
			if(a[i] <= k) ++cnt;
			if(a[i - l] <= k) --cnt;
			res = max(res,cnt);
		}
		cout << l - res << endl;
	}
}

