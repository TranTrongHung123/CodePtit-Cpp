#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

// Hoan vi lap

ll giai_thua(ll n){
	ll res = 1;
	for(ll i = 1; i <= n; i++){
		res *= i;
	}
	return res;
}

ll solve(ll n, ll r, ll b, ll g){
	ll cnt = 0;
	for(ll R = r; R <= n-g-b; R++){
		for(ll G = g; G <= n-R-b; G++){
			cnt += (giai_thua(n) / (giai_thua(R) * giai_thua(G) * giai_thua(n-R-G)));
		}
	}
	return cnt;
}

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int TC;
	cin >> TC;
	while(TC--){
		ll n, r, b, g;
		cin >> n >> r >> b >> g;
		cout << solve(n,r,b,g) << endl;
	}
}

