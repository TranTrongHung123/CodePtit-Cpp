#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int n;
	map<int,int> mp;
	vector<int> vt;
	while(cin >> n){
		vt.push_back(n);
		++mp[n];
	}
	for(auto x : vt){
		if(mp[x] != 0){
			cout << x << " " << mp[x] << endl;
			mp[x] = 0;
		}
	}
}

