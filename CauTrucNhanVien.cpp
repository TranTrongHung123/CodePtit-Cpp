struct NhanVien{
	string mnv = "00001";
	string ten, gtinh, ngaysinh, dchi, mst, ngayki;
};

void nhap(NhanVien &a){
	getline(cin, a.ten);
	cin >> a.gtinh >> a.ngaysinh;
	cin.ignore();
	getline(cin, a.dchi);
	cin >> a.mst >> a.ngayki;
}

