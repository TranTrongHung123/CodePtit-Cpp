#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		int dp[n]; // Luu do dai day con tang dai nhat tu chi so dau tien den chi so i
		fill(dp, dp + n, 1); // Truong hop co so day co 1 phan tu
		for(int i = 0; i < n; i++){
			for(int j = 0; j < i; j++){
				if(a[i] > a[j]){
					dp[i] = max(dp[i], dp[j] + 1);
				}
			}
		}
		cout << *max_element(dp, dp + n) << endl;
	}
}

