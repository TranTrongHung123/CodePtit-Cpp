// Luu cac phan tu theo thu tu giam dan vao stack
// Xet tu cuoi ve tu chi so j, neu a[j] lon hon phan tu o dinh stack thi cung lon hon phan tu o phia sau
// nen ta se kheo rong khoang cach ra

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		stack<int> stk; // Luu lai chi so i
		for(int i = 0; i < n; i++){
			if(stk.empty() || a[stk.top()] >= a[i]){ // Luu cac phan tu theo thu tu giam dan ban dau cua mang
				stk.push(i);
			}
		}
		int res = 0;
		for(int i = n-1; i >= 0; i--){
			while(!stk.empty() && a[i] >= a[stk.top()]){
				res = max(res, i - stk.top());
				stk.pop();
			}
		}
		cout << res << endl;
	}
}

