#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int TC;
	cin >> TC;
	while(TC--){
		int n;
		cin >> n;
		int a[n][n];
		int sum_matrix = 0; // Luu tong cac phan tu cua ma tran
		for(int i = 0; i < n; i++){
			for(int j = 0; j < n; j++){
				cin >> a[i][j];
				sum_matrix += a[i][j];
			}
		}
		int sum_row[n] = {0}; // Luu tong phan tu cac hang
		int sum_col[n] = {0}; // Luu tong phan tu cac cot
		for(int i = 0; i < n; i++){
			for(int j = 0; j < n; j++){
				sum_row[i] += a[i][j];
				sum_col[j] += a[i][j];
			}
		}
		int res = 0; // Tim hang hoac cot co tong lon nhat
		for(int i = 0; i < n; i++){
			res = max({res, sum_row[i], sum_col[i]});
		}	
		cout << n * res - sum_matrix << endl;	
	}
}

