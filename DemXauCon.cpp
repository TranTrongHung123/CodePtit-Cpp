#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		string s;
		int k;
		cin >> s >> k;
		int res = 0;
		for(int i = 0; i < s.size(); i++){
			int dis = 0;
			int mark[256];
			memset(mark, 0, sizeof(mark));
			for(int j = i; j < s.size(); j++){
				if(mark[s[j]] == 0){
					++dis;
				}
				++mark[s[j]];
				if(dis == k) ++res;
				if(dis > k) break;
			}
		}
		cout << res << endl;
	}
}

