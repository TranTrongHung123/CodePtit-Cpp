#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		string s1, s2;
		cin >> s1 >> s2;
		int mark1[256] = {0}, mark2[256] = {0};
		for(auto x : s2){
			++mark2[x];
		}
		int cnt = 0, L = 0;
		int res = 1e9; // Luu do dai nho nhat
		int index = -1; // Luu vi tri bat dau cua xau con nho nhat
		for(int i = 0; i < s1.size(); i++){
			++mark1[s1[i]];
			if(mark1[s1[i]] <= mark2[s1[i]]){
				++cnt;
			}
			if(cnt == s2.size()){
				while((mark2[s1[L]] == 0) || (mark1[s1[L]] > mark2[s1[L]])){
					if(mark1[s1[L]] > mark2[s1[L]]){
						--mark1[s1[L]];
					}
					++L;
				}
				if((i - L + 1) < res){
					res = i - L + 1;
					index = L;
				}
			}
		}
		if(index == -1){
			cout << -1 << endl;
		}
		else{
			cout << s1.substr(index, res) << endl;
		}
	}
}

