#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n+1][n+1];
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= n; j++){
				cin >> a[i][j];
			}
		}
		map<int,int> mp;
		for(int j = 1; j <= n; j++){
			mp[a[1][j]] = 1;
		}
		for(int i = 2; i <= n; i++){
			for(int j = 1; j <= n; j++){
				if(mp[a[i][j]] == i-1){
					mp[a[i][j]] = i;
				}
			}
		}	
		int cnt = 0;
		for(auto x : mp){
			if(x.second == n){
				++cnt;
			}
		}			
		cout << cnt << endl;
	}
}

