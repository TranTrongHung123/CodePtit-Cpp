#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int TC;
	cin >> TC;
	while(TC--){
		int n;
		cin >> n;
		ll a[n];
		for(auto &x : a) cin >> x;
		ll res = 0;
		for(int i = 0; i < n; i++){
			ll s = 1;
			for(int j = i; j < n; j++){
				s *= a[j];
				res = max(res, s);
			}
		}
		cout << res << endl;
	}
}

