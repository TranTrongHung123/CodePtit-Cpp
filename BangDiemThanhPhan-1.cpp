#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

struct SinhVien{
	string msv, ten, lop;
	double x, y, z;
};

void nhap(SinhVien& a){
	cin >> a.msv;
	scanf("\n");
	getline(cin, a.ten);
	cin >> a.lop >> a.x >> a.y >> a.z;
}

void in_ds(SinhVien ds[], int n){
	for(int i = 0; i < n; i++){
		cout << i + 1 << " " << ds[i].msv << " " << ds[i].ten << " " << ds[i].lop << " " << fixed << setprecision(1) << ds[i].x << " " << fixed << setprecision(1) << ds[i].y << " " << fixed << setprecision(1) << ds[i].z << endl;
	}
}

bool cmp(SinhVien a, SinhVien b){
	return a.msv < b.msv;
}

void sap_xep(SinhVien ds[], int n){
	sort(ds, ds + n, cmp);
}

int main(){
    int n;
    cin >> n;
    struct SinhVien *ds = new SinhVien[n];
    for(int i = 0; i < n; i++) {
    	nhap(ds[i]);
	}
	sap_xep(ds, n);
    in_ds(ds,n);
    return 0;
}

