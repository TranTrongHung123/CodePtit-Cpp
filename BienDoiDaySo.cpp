#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		int l = 0, r = n-1, res = 0;
		while(l <= r){
			if(a[l] == a[r]){
				++l;
				--r;
			}
			else if(a[l] > a[r]){
				++res;
				--r;
				a[r] = a[r] + a[r+1];
			}
			else{
				++res;
				++l;
				a[l] = a[l] + a[l-1];
			}
		}
		cout << res << endl;
	}
}

