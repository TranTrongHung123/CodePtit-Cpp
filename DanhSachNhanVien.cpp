#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

struct NhanVien{
	string ten, gioitinh, ngaysinh, diachi, mst, ngayki;
};

int x = 0;

void nhap(NhanVien &a){
	++x;
	if(x == 1){
		cin.ignore();
	}
	getline(cin, a.ten);
	getline(cin, a.gioitinh);
	getline(cin, a.ngaysinh);
	getline(cin, a.diachi);
	getline(cin, a.mst);
	getline(cin, a.ngayki);
}

void inds(NhanVien ds[], int N){
	for(int i = 0; i < N; i++){
		string t = to_string(i + 1);
		cout << string(5 - t.size(), '0') << t << " " << ds[i].ten << " " << ds[i].gioitinh << " " << ds[i].ngaysinh << " " << ds[i].diachi << " " << ds[i].mst << " " << ds[i].ngayki << endl;
	}
}

int main(){
    struct NhanVien ds[50];
    int N,i;
    cin >> N;
    for(i = 0; i < N; i++) nhap(ds[i]);
    inds(ds,N);
    return 0;
}

