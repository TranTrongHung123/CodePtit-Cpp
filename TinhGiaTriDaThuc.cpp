#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const ll mod = 1e9 + 7;

int main(){
    ll t;
    cin >> t;
    while(t--){
        ll n, x;
        cin >> n >> x;
        ll a[n];
        for(auto &x : a) cin >> x;
        ll p = 0, res = 1;
        for(ll i = n-1; i >= 0; i--){
            p += a[i] * res;
            p %= mod;
            res *= x;
            res %= mod;
        }
        cout << p % mod << endl;
    }
}

