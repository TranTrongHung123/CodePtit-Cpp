#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

struct DN{
	string ma, ten;
	int soluong;	
};

bool cmp(DN a, DN b){
	if(a.soluong == b.soluong) return a.ma < b.ma;
	return a.soluong > b.soluong;
}

int main(){
	int n;
	cin >> n;
	DN a[n];
	for(int i = 0; i < n; i++){
		scanf("\n");
		getline(cin, a[i].ma);
		getline(cin, a[i].ten);
		cin >> a[i].soluong;
	}
	sort(a, a + n, cmp);
	for(int i = 0; i < n; i++){
		cout << a[i].ma << " " << a[i].ten << " " << a[i].soluong << endl;
	}	
}

