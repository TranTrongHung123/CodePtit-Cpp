#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int dx[4] = {-1, 0, 1, 0};
int dy[4] = {0, 1, 0, -1};

int n, m;
string a[50][50];

void loang(int i, int j){
	a[i][j] = "#";
	for(int k = 0; k < 4; k++){
		int i1 = i + dx[k];
		int j1 = j + dy[k];
		if(i1 >= 1 && i1 <= n && j1 >= 1 && j1 <= m && a[i1][j1] == "O"){
			loang(i1,j1);
		}
	}
}

