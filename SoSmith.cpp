#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int tcs(int n){
	int s = 0;
	while(n){
		s += n % 10;
		n /= 10;
	}
	return s;
}

int prime(int n){
	for(int i = 2; i <= sqrt(n); i++){
		if(n % i == 0){
			return 0;
		}
	}
	return n>1;
}

bool check(int n){
	if(prime(n)){
		return false;
	}
	int x = tcs(n), tmp = 0;
	for(int i = 2; i <= sqrt(n); i++){
		if(n % i == 0){
			while(n % i == 0){
				tmp += tcs(i);
				n /= i;
			}
		}
	}
	if(n != 1){
		tmp += tcs(n);
	}
	if(x == tmp){
		return true;
	}
	return false;
}

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		if(check(n)){
			cout << "YES" << endl;
		}
		else{
			cout << "NO" << endl;
		}
	}
}

