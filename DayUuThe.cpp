#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	int t;
	cin >> t;
	while(t--){
		int s = 0, cnt_c = 0, cnt_l = 0;
		char c = ' ';
		while(c == ' '){
			int n;
			cin >> n;
			++s;
			if(n % 2 == 0) ++cnt_c;
			else ++cnt_l;
			c = getchar(); // Doc bo dem ban phim
		}
		if(((s % 2 == 0) && (cnt_c > cnt_l)) || ((s % 2 != 0) && (cnt_c < cnt_l))) cout << "YES" << endl;
		else cout << "NO" << endl;
	}
}

