#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

// s1 : tong cac phan tu tap 1, s2 : tong cac phan tu tap 2
// s1 + s2 = n*(n+1)/2 = s
// s2 - s1 = m
// => s2 = (s + m) / 2 
// => s1 = s2 - m

bool check(ll n, ll m){
	ll s = n * (n + 1) / 2;
	ll s2 = (s + m) / 2;
	ll s1 = s2 - m;
	if(s1 + s2 == s && s2 - s1 == m && __gcd(s1,s2) == 1){
		return true;
	}
	return false;
}

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		ll n, m;
		cin >> n >> m;
		if(check(n,m)) cout << "Yes" << endl;
		else cout << "No" << endl;
	}
}

