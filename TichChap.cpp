#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int TC;
	cin >> TC;
	while(TC--){
		int n, m;
		cin >> n >> m;
		ll a[n][m];
		for(int i = 0; i < n; i++){
			for(int j = 0; j < m; j++){
				cin >> a[i][j];
			}
		}
		ll ker[3][3];
		for(int i = 0; i < 3; i++){
			for(int j = 0; j < 3; j++){
				cin >> ker[i][j];
			}
		}
		ll sum = 0;
		for(int i = 0; i <= n-3; i++){
			for(int j = 0; j <= m-3; j++){
				for(int h = i; h <= i+2; h++){
					for(int k = j; k <= j+2; k++){
						sum += a[h][k] * ker[h-i][k-j];
					}
				}
			}
		}
		cout << sum << endl;
	}
}

