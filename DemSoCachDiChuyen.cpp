#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int n, k;
int a[50][50];
int dx[2] = {1, 0};
int dy[2] = {0, 1};
int cnt, sum;

void Try(int i, int j){
	if(i == (n-1) && j == (n-1) && sum == k){
		++cnt;
	}
	for(int k = 0; k < 2; k++){
		int i1 = i + dx[k];
		int j1 = j + dy[k];
		if(i1 >= 0 && i1 <= n-1 && j1 >= 0 && j1 <= n-1){
			sum += a[i1][j1];
			Try(i1,j1);
			sum -= a[i1][j1];
		}
	}
}

int main(){
	int t;
	cin >> t;
	while(t--){
		cin >> n >> k;
		for(int i = 0; i < n; i++){
			for(int j = 0; j < n; j++){
				cin >> a[i][j];
			}
		}
		sum = a[0][0];
		cnt = 0;
		Try(0,0);
		cout << cnt << endl;
	}
}

