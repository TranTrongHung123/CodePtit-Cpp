#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		vector<int> a(n);
		for(auto &x : a) cin >> x;
		int k, x;
		cin >> k >> x;
		int idx;
		for(int i = 0; i < n; i++){
			if(a[i] == x){
				idx = i;
				break;
			}
		}		
		int l = idx - k/2;
		int r = idx + k/2;
		for(int i = l; i <= r; i++){
			if(i != idx){
				cout << a[i] << " ";
			}
		}
		cout << endl;
	}
}

