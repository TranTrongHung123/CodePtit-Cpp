#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n, k;
		cin >> n >> k;
		int a[n];
		for(auto &x : a) cin >> x;
		sort(a, a + n);
		ll cnt = 0;
		for(int i = 0; i < n-1; i++){
			auto it = lower_bound(a + i + 1, a + n, a[i] + k);
			cnt += (it - a) - 1 - i;
		}
		cout << cnt << endl;
	}
}

