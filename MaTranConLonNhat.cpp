#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int dp[505][505];

int main(){
	ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int T;
	cin >> T;
	while(T--){
		int n, m;
		cin >> n >> m;
		int a[n+1][m+1];
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				cin >> a[i][j];
			}
		}
		memset(dp, 0, sizeof(dp));
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				if(a[i][j] == 1){
					dp[i][j] = min({dp[i-1][j], dp[i-1][j-1], dp[i][j-1]}) + 1;
				}
				else{
					dp[i][j] = 0;
				}
			}
		}
		int res = 0;
		for(int i = 1; i <= n; i++){
			for(int j = 1; j <= m; j++){
				res = max(res, dp[i][j]);
			}
		}		
		cout << res << endl;
	}
}

