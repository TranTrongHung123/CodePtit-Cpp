#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n, x;
		cin >> n >> x;
		int a[n];
		for(auto &k : a) cin >> k;
		sort(a, a + n);
		bool ok = false;
		for(int i = 0; i < n-1; i++){
			auto check1 = binary_search(a + i + 1, a + n, x + a[i]);
			auto check2 = binary_search(a + i + 1, a + n, a[i] - x);
			if(check1 || check2){
				ok = true;
				break;
			}
		}
		if(ok) cout << 1 << endl;
		else cout << -1 << endl;
	}
}

