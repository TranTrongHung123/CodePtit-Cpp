#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

ll solve(string b, ll a){ // b % a
	ll res = 0;
	for(auto x : b){
		res = (10 * res + (x - '0')) % a;
	}
	return res;
}

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		ll a;
		string b;
		cin >> a >> b;
		ll x = solve(b,a);
		cout << __gcd(x,a) << endl;
	}
}

