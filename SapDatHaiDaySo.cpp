#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n, m;
		cin >> n >> m;
		int a[n], b[m];
		for(auto &x : a) cin >> x;
		for(auto &x : b) cin >> x;
		sort(a, a + n);
		map<int,int> mp;
		for(auto x : a) ++mp[x];
		for(int i = 0; i < m; i++){
			if(mp[b[i]] != 0){
				for(int l = 1; l <= mp[b[i]]; l++){
					cout << b[i] << " ";
				}
				mp[b[i]] = 0;
			}
		}
		for(auto x : a){
			if(mp[x]){
				cout << x << " ";
			}
		}	
		cout << endl;
	}
}

