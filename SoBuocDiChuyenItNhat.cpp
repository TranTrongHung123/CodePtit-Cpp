#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a) cin >> x;
		vector<int> dp(n, 1e9); // Luu so buoc it nhat de den duoc dp[i]
		dp[0] = 0; // Xuat phat tu o dau tien nen so buoc bat dau la 0
		for(int i = 1; i < n; i++){
			for(int j = 0; j < i; j++){
				// a[j] : So buoc toi da co the nhay duoc tu vi tri j
				// (i-j) : la khoang cach tu a[i] den a[j]
				if(a[j] >= (i-j)){ // De tu vi tri j nhay den i thi so buoc can lon hon hoac bang khoang cach
					dp[i] = min(dp[i], dp[j] + 1);
				}
			}
		}
		if(dp[n-1] == 1e9){
			cout << -1 << endl;
		}
		else{
			cout << dp[n-1] << endl;
		}
	}
}

