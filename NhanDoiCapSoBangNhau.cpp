#include <bits/stdc++.h>
#define endl "\n"
using namespace std;
typedef long long ll;
const int mod = 1e9 + 7;

int main(){
	ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
	int t;
	cin >> t;
	while(t--){
		int n;
		cin >> n;
		int a[n];
		for(auto &x : a){
			cin >> x;
		}
		for(int i = 1; i < n; i++){
			if(a[i] != 0 && a[i] == a[i-1]){
				a[i] = 0;
				a[i-1] *= 2;
			}
		}
		vector<int> vt;
		int cnt = 0;
		for(auto x : a){
			if(x == 0){
				++cnt;
			}
			else{
				vt.push_back(x);
			}
		}
		for(auto x : vt){
			cout << x << " ";
		}
		for(int i = 1; i <= cnt; i++){
			cout << "0 ";
		}
		cout << endl;
	}
}

